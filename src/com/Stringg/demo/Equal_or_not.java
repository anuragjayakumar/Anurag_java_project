package com.Stringg.demo;

public class Equal_or_not {
	public static void main(String[] args) {
		String s = "welcome to facebook";
		 boolean flag_Equals  = s.equals("Welcome to facebook");
		 System.out.println("Equal or not :"+ flag_Equals);
		 
		 
		  boolean ignorecase = s.equalsIgnoreCase("Welcome to facebook");
		  System.out.println("Equal ignore case :"+ ignorecase);
		 
		 
		
		
	}
	
	
	

}
