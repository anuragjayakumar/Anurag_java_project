package com.loop.demo;

public class Traingle_Forloop {
	public static void main(String[] args) {
		System.out.println("Start");
		for (int i = 1; i <= 3; i++) {
			for (int j = 1; j <= i; j++) {
				System.out.print(i +" ");
				
			}
			System.out.println();
			
		}
		System.out.println();
		System.out.println("End");
		
	}

}
