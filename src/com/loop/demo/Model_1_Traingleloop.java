package com.loop.demo;

public class Model_1_Traingleloop {
	public static void main(String[] args) {
		System.out.println("Start");
		for (int i = 3; i>= 1; i--) {
			for (int j = 1; j<= 3; j++) {
				System.out.print(i + " ");
				
			}
			System.out.println();
			
		}
		System.out.println();
		System.out.println("End");
		
	}

}
